"""Create compact review-evaluation summaries for the paper and artifact."""

import json
import statistics
from pathlib import Path

ROOT = Path(__file__).parent
source = ROOT / "data" / "review_evaluation" / "results.json"
data = json.loads(source.read_text())

summary = {"schema_version": 1, "source": str(source.relative_to(ROOT)), "groups": {}}
for encoder in data["encoders"]:
    for language in ("en", "id"):
        runs = [run for run in data["runs"] if run["encoder"] == encoder and run["language"] == language]
        key = f"{encoder}/{language}"
        summary["groups"][key] = {
            metric: statistics.mean(run["metrics"][metric] for run in runs)
            for metric in ("citation_validity", "bracket_format_compliance", "source_coverage",
                           "section_completion", "word_count")
        }
    pairs = [row for row in data["paired_results"] if row["encoder"] == encoder]
    summary["groups"][f"{encoder}/paired"] = {
        "cited_evidence_jaccard": statistics.mean(row["cited_evidence_jaccard"] for row in pairs)
    }

summary["interpretation"] = (
    "Structural grounding only. The metrics do not establish factual correctness, review quality, or human preference."
)
out = ROOT / "data" / "review_evaluation" / "summary.json"
out.write_text(json.dumps(summary, indent=2) + "\n")

def pct(value):
    return f"{100 * value:.1f}"

macros = ["% Generated by summarize_review_evaluation.py; do not edit manually."]
names = {
    "ReviewClipEnCitationValidity": pct(summary["groups"]["clip/en"]["citation_validity"]),
    "ReviewClipIdCitationValidity": pct(summary["groups"]["clip/id"]["citation_validity"]),
    "ReviewMclipEnCitationValidity": pct(summary["groups"]["mclip/en"]["citation_validity"]),
    "ReviewMclipIdCitationValidity": pct(summary["groups"]["mclip/id"]["citation_validity"]),
    "ReviewClipEnSourceCoverage": pct(summary["groups"]["clip/en"]["source_coverage"]),
    "ReviewClipIdSourceCoverage": pct(summary["groups"]["clip/id"]["source_coverage"]),
    "ReviewMclipEnSourceCoverage": pct(summary["groups"]["mclip/en"]["source_coverage"]),
    "ReviewMclipIdSourceCoverage": pct(summary["groups"]["mclip/id"]["source_coverage"]),
    "ReviewClipEvidenceJaccard": f"{summary['groups']['clip/paired']['cited_evidence_jaccard']:.3f}",
    "ReviewMclipEvidenceJaccard": f"{summary['groups']['mclip/paired']['cited_evidence_jaccard']:.3f}",
}
for name, value in names.items():
    macros.append(f"\\newcommand{{\\{name}}}{{{value}}}")
(ROOT / "paper" / "generated_review_results.tex").write_text("\n".join(macros) + "\n")
print(f"Wrote {out} and paper/generated_review_results.tex")
